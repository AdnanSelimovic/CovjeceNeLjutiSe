class Piece (val pieceId: Int, val color: String, val pieceName: String) {
    // Getter for pieceId
    def getPieceId(): Int = {
        pieceId
    }

    // Getter for color
    def getColor(): String = {
        color
    }

    // Getter for pieceName
    def getPieceName(): String = {
        pieceName
    }
}
